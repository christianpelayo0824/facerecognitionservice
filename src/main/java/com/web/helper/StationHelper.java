package com.web.helper;

public class StationHelper {

	private int sid;
	private String station;

	public int getSid() {
		return sid;
	}

	public StationHelper setSid(int sid) {
		this.sid = sid;
		return this;
	}

	public String getStation() {
		return station;
	}

	public StationHelper setStation(String station) {
		this.station = station;
		return this;
	}

}
